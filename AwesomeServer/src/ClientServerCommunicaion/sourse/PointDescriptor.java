package ClientServerCommunicaion.sourse;

import java.awt.*;

public class PointDescriptor {

    private Color color;
    private int size;
    private int x;
    private int y;

    public PointDescriptor(Color color, int size, int x, int y) {
        this.color = color;
        this.size = size;
        this.x = x;
        this.y = y;
    }
}
